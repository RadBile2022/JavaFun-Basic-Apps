    private void btnTbhMakanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTbhMakanActionPerformed
        if (cekKosong(true) == false) {
            String menuItem = cboMakan.getSelectedItem().toString();
            if (cekDataTabel(menuItem) == false) {
                int hrg = Integer.parseInt(txtHargaMakan.getText());
                int kuantiti = Integer.parseInt(txtQtyMakan.getText());
                txtSubtotalMakan.setText("" + (hrg * kuantiti));

                menu.add(menuItem);
                harga.add(txtHargaMakan.getText());
                qty.add(txtHargaMakan.getText());
                subtotal.add(txtHargaMakan.getText());

                setTabel();
            }
        }
    }//GEN-LAST:event_btnTbhMakanActionPerformed


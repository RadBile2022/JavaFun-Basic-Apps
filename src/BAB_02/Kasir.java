    // A. METHOD KOSONG
    private boolean cekKosong(boolean makan) {
        boolean hasil = false;

        if (makan == true) {
            if (txtQtyMakan.getText().isEmpty() == true) {
                JOptionPane.showMessageDialog(null, "Qty makanan harus diisi");
                hasil = true;
            }
        } else {
            if (txtQtyMinum.getText().isEmpty() == true) {
                JOptionPane.showMessageDialog(null, "Qty minuman harus diisi");
                hasil = true;
            }
        }
        return hasil;
    }

    // B. METHOD CEK DATA TABEL
    private boolean cekDataTabel(String data) {
        boolean hasil = menu.contains(data);
        if (hasil == true) {
            JOptionPane.showMessageDialog(null, "Menu sudah ditambahkan");
        }
        return hasil;
    }

    // C. METHOD SET HARGA MAKANAN
    private void setHargaMakanan() {
        int harga = 0;
        int pil = cboMakan.getSelectedIndex();
        switch (pil) {
            case 1:
                harga = 25000;
                break;
            case 2:
                harga = 25000;
                break;
            case 3:
                harga = 35000;
                break;
            case 4:
                harga = 35000;
                break;
            case 5:
                harga = 40000;
                break;
        }
        txtHargaMakan.setText("" + harga);
        try {
            int qty = Integer.parseInt(txtQtyMakan.getText());
            txtSubtotalMakan.setText("" + harga * qty);
        } catch (NumberFormatException e) {
            txtSubtotalMakan.setText("0");
        }
    }

    // D. METHOD SET HARGA MINUMAN
    private void setHargaMinuman() {
        int harga;
        if (cboMinum.getSelectedIndex() == 0) {
            harga = 0;
        } else {
            harga = 20000;
        }
        txtHargaMinum.setText("" + harga);
        try {
            int qty = Integer.parseInt(txtQtyMinum.getText());
            txtSubtotalMinum.setText("" + harga * qty);
        } catch (NumberFormatException e) {
            txtSubtotalMinum.setText("0");
        }
    }

    // E. METHOD SET TABEL
    private void setTabel() {
        int total = 0;
        double pajak = 0;
        int bayar = 0;

        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Menu");
        tbl.addColumn("Qty");
        tbl.addColumn("Harga");
        tbl.addColumn("Sub Total");

        for (int n = 0; n < menu.size(); n++) {
            total = total + Integer.parseInt(subtotal.get(n).toString());

            tbl.addRow(new Object[]{
                menu.get(n),
                qty.get(n),
                harga.add(n),
                subtotal.get(n),});
        }

        pajak = total * 0.1;
        bayar = (int) (total + pajak);

        tableData.setModel(tbl);
        txtTotal.setText("" + total);
        txtPajak.setText("" + pajak);
        txtBayar.setText("" + bayar);
    }

